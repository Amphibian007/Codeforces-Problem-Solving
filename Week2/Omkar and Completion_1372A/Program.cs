using System;

namespace Omkar_and_Completion_1372A
{
    class Program
    {
        static void Main(string[] args)
        {
            int T = int.Parse(Console.ReadLine());
            {
                for(int i = 0; i < T; i++)
                {
                    int n = int.Parse(Console.ReadLine());
                    for (int j = 1; j <= n; j++)
                        Console.Write(3+ " ");
                    Console.WriteLine();
                }
            }
            
        }
    }
}
